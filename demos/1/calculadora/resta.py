# Resta de 2 números enteros de hasta 3 digitos.
print("Ingresa el primer número a restar de hasta 3 digitos, por favor.") # Se le pide al usuario que ingrese el primer número entero de hasta 3 digitos.
num1 = int(input()) # Caja receptora de valor.
print("Ingresa el segundo número a restar de hasta 3 digitos, por favor.") # Se le pide al usuario que ingrese el segundo número entero de hasta 3 digitos.
num2 = int(input()) # Caja receptora de valor.
resultado = num1 - num2 # Resta de las variables.
print("El resultado es:" + " " + str(resultado)) # Resultado de la resta.
