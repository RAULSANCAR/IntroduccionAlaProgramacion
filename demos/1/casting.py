print("Ingresa el primer número entero a sumar de hasta 3 digitos, por favor.") # Se le pide al usuario que ingrese el primer número entero 
num1 = int(input()) # Caja receptora de valor.
print("Ingresa el segundo número entero de hasta 3 digitos y hasta 2 posiciones después del punto a sumar, por favor.") # Se le pide al usuario que ingrese el primer número entero con 2 posiciones después del punto.
num2 = float(input()) # Caja receptora de valor.
